package First_package;

public class Unary_operators1 {

	public static void main(String[] args) {
		int i = 20; // local variable
		System.out.println(i++); // 20
		System.out.println(i--); // 21
		System.out.println(++i); // 21
		System.out.println(--i); // 20
		System.out.println(i); // 20

		int j = i + 1;
		System.out.println(j); // 21

	}

}
